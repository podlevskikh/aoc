func (a *ALU) proc(instr *Instruction, in int) {
	switch instr.Operator {
	case "inp":
		a.inp(instr.To, in)
	case "add":
		a.add(instr.To, instr.From, instr.Input)
	case "mul":
		a.mul(instr.To, instr.From, instr.Input)
	case "mod":
		a.mod(instr.To, instr.From, instr.Input)
	case "div":
		a.div(instr.To, instr.From, instr.Input)
	case "eql":
		a.eq(instr.To, instr.From, instr.Input)
	}
}

func (a *ALU) inp(inTo string, in int) {
	a.Vars[inTo] = in
}

func (a *ALU) add(to, from string, inp int) {
	if from != "" {
		inp = a.Vars[from]
	}
	a.Vars[to] += inp
}

func (a *ALU) mul(to, from string, inp int) {
	if from != "" {
		inp = a.Vars[from]
	}
	a.Vars[to] *= inp
}

func (a *ALU) div(to, from string, inp int) {
	if from != "" {
		inp = a.Vars[from]
	}
	a.Vars[to] /= inp
}

func (a *ALU) mod(to, from string, inp int) {
	if from != "" {
		inp = a.Vars[from]
	}
	a.Vars[to] -= (a.Vars[to] / inp)*26
}

func (a *ALU) eq(to, from string, inp int) {
	if from != "" {
		inp = a.Vars[from]
	}
	if inp == a.Vars[to] {
		a.Vars[to] = 1
	} else {
		a.Vars[to] = 0
	}
}

func parseMonad(in int) []int {
	res := []int{}
	for _, inStr := range strings.Split(strconv.Itoa(in), "") {
		inInt, _ := strconv.Atoi(inStr)
		if inInt == 0 {
			return []int{}
		}
		res = append(res, inInt)
	}
	return res
}

